# Contoh pengulangan for sederhana dengan nama Latif
angka = [10, 20, 30, 40, 50]
for x in angka:
    print("Latif memilih angka", x)

# Contoh pengulangan for dengan daftar makanan
makanan = ["bakso", "sate", "nasi goreng"]
for item in makanan:
    print("Latif suka makan", item)

# Pengulangan dengan range dan nama Latif
for i in range(5, 11):
    print("Latif sedang menghitung:", i)
